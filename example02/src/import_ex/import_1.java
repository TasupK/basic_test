package import_ex;

public class import_1 {
	/*
	 * import문
	 * 클래스를 사용할 때 패키지 이름을 생략할 수 있다.
	 * 컴파일러에게 클래스가 속한 패키지를 알려준다.
	 * 
	 * *** 원래는 이렇게 ***
	 * class ImportTest{
	 * 
	 * java.util.Date today = new java.util.Date();
	 * }
	 * 
	 * *** 이렇게 한다. ***
	 * import java.util.Date;
	 * 
	 * class ImportTest{
	 * 
	 * Date today = new Date();
	 * }
	 * 
	 * 이클립스 단축키
	 * 컨트롨 시프트 + O
	 * 
	 * java.lang 패키지의 클래스는 import 하지 않고도 사용할 수 있다.
	 * String, Object, System, Thread
	 * 기본 패키지
	 * 
	 * 
	 * import문은 패키지 문과 클래스 선언 사이에 선언한다.
	 * 
	 * 가끔 이런 경우가 있다.
	 * 이름이 같은 클래스가 속한 두 패키지를 import 할 때는 클래스 앞에 패키지 명을 붙여야 한다.
	 * import java.sql.Date
	 * import java.util.Date
	 */

}
